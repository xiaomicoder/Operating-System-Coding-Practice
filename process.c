#include <stdio.h>
#include <unistd.h>
#include <time.h>
#include <sys/times.h>

#define HZ    100

void cpuio_bound(int last, int cpu_time, int io_time);

int main(int argc, char * argv[])
{
    pid_t subp1,subp2,subp3;
    /*
    fork返回值<0时，创建新进程错误
	fork返回值=0时，块内容为子进程所执行
	fork返回值>0时，块内容为父进程所执行
	*/
	subp1 = fork();
	if(subp1 < 0) {
		printf("Fail to create new process!\n");
	} else if(subp1 == 0){
		cpuio_bound(10,0,5);
	} else {
		printf("New process created:%d\n",subp1);
		waitpid(subp1,NULL,0);
	}
	subp2 = fork();
	if(subp2 < 0) {
		printf("Fail to create new process!\n");
	} else if(subp2 == 0){
		cpuio_bound(10,4,0);
	} else {
		printf("New process created:%d\n",subp2);
		waitpid(subp2,NULL,0);
	}
	subp3 = fork();
	if(subp3 < 0) {
		printf("Fail to create new process!\n");
	} else if(subp3 == 0){
		cpuio_bound(10,2,3);
	} else {
		printf("New process created:%d\n",subp3);
		waitpid(subp3,NULL,0);
	}
    return 0;
}

/*
 * 此函数按照参数占用CPU和I/O时间
 * last: 函数实际占用CPU和I/O的总时间，不含在就绪队列中的时间，>=0是必须的
 * cpu_time: 一次连续占用CPU的时间，>=0是必须的
 * io_time: 一次I/O消耗的时间，>=0是必须的
 * 如果last > cpu_time + io_time，则往复多次占用CPU和I/O
 * 所有时间的单位为秒
 */
void cpuio_bound(int last, int cpu_time, int io_time)
{
    struct tms start_time, current_time;
    clock_t utime, stime;
    int sleep_time;

    while (last > 0)
    {
        /* CPU Burst */
        times(&start_time);
        /* 其实只有t.tms_utime才是真正的CPU时间。但我们是在模拟一个
         * 只在用户状态运行的CPU大户，就像“for(;;);”。所以把t.tms_stime
         * 加上很合理。*/
        do
        {
            times(&current_time);
            utime = current_time.tms_utime - start_time.tms_utime;
            stime = current_time.tms_stime - start_time.tms_stime;
        } while ( ( (utime + stime) / HZ )  < cpu_time );
        last -= cpu_time;

        if (last <= 0 )
            break;

        /* IO Burst */
        /* 用sleep(1)模拟1秒钟的I/O操作 */
        sleep_time=0;
        while (sleep_time < io_time)
        {
            sleep(1);
            sleep_time++;
        }
        last -= sleep_time;
    }
}
